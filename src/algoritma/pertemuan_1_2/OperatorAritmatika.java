package algoritma.pertemuan_1_2;

public class OperatorAritmatika {
    /*
    - kurang
    + tambah
    * perkalian
    / pembagian

     */
// menjalankans sebuah instruksi
    public static void main(String[] args) {
        int a  = 10;
        int b = 5;
        System.out.println("perkalian= "+(a*b));
        System.out.println("pembagian= "+(a/b));
        System.out.println("kurang= "+(a-b));
        System.out.println("tambah= "+(a+b));
        System.out.println("modulus= "+(6 %4));

    }
}
