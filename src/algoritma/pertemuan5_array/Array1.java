package algoritma.pertemuan5_array;

public class Array1 {
	public static void main(String[] args) {
		String[] arrayStr = new String[3];
		arrayStr[0] = "400";
		arrayStr[1] = "500";
		arrayStr[2] = "600";
	
		for (int i = 0; i < arrayStr.length; i++) {
			System.out.println(arrayStr[i]);
			System.out.println(i);
		}
		 
	}

}
